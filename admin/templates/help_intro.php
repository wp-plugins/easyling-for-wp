<p>Integrating Easyling into Wordpress has never been easier. With this plugin you are' .
    'able to focus on what is really important: content.</p>

<p><a href="http://easyling.com/?utm_source=easyling-wp-plugin-admin&utm_medium=admin-link&utm_campaign=easyling-wp" target="_blank">Easyling</a> is a proxy-based website 
    translation solution. It crawls your website, finds all the pages and texts. 
    Then you can do the translation yourself using our in-context, 
    live editing/preview/review editor, or export as an XLIFF file and do the 
    translation in your favourite CAT tool. As an alternative, just leave all the 
    work to us and <a href="http://www.easyling.com/website-owners/?utm_source=easyling-wp-plugin-admin&utm_medium=admin-link&utm_campaign=easyling-wp" target="_blank">order a website translation from Easyling</a>.</p>

<p>Once the translation is done, this Easyling for Wordpress plugin does the magic: 
    automatically downloads your translations from Easyling only once, 
and presents it to your visitors using domain-based or URL-based configuration, i.e.:</p>

<ul>
    <li>www.example.com for the main language</li>
    <li>www.example.com/de/ for the German translation</li>
</ul>
<p>Or</p>
<ul>
    <li>www.example.com for the main language</li>
    <li>www.example.de for the German translation</li>
</ul>
<p>Or</p>
<ul>
    <li>www.example.com for the main language</li>
    <li>de.example.com for the German translation</li>
</ul>